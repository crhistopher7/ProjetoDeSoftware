    private void jButtonUpdateProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateProdutoActionPerformed
        if (jTFsNotEmpty()) {
            String codigo = jTextFieldCodigo.getText();
            String produto = jTextFieldProduto.getText();
            String quantidade = jTextFieldQuantidade.getText();
            String tipo = jTextFieldTipo.getText();
            String fornecedor = jTextFieldFornecedor.getText();

            p.setCodigo(Integer.parseInt(codigo));
            p.setNome(produto);
            p.setFornecedor(fornecedor);
            p.setTipo(tipo);
            p.setQuantidade(Float.parseFloat(quantidade));

            if (crud.updateProduct(p)) {
                JOptionPane.showMessageDialog(null, "Produto Editado!");
                this.up = true;
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Produto NÃ£o Editado!\nTente novamente");
            }
       
        } else {
            JOptionPane.showMessageDialog(null, "Preencha os campos!");
        }
        
        
    }//GEN-LAST:event_jButtonUpdateProdutoActionPerformed

